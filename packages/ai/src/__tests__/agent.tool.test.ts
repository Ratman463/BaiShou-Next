import { describe, it, expect } from 'vitest';
import { AgentTool, ToolContext } from '../tools/agent.tool';
import { z } from 'zod';

class MockWeatherTool extends AgentTool<z.ZodObject<{ location: z.ZodString }>> {
  readonly name = 'get_weather';
  readonly description = 'Get current weather for a location';
  readonly parameters = z.object({
    location: z.string(),
  });

  async execute(args: { location: string }, context: ToolContext): Promise<string> {
    return `Weather in ${args.location} (Vault: ${context.vaultName}) is Sunny.`;
  }
}

describe('AgentTool', () => {
  it('should define basic properties correctly', () => {
    const weatherTool = new MockWeatherTool();
    expect(weatherTool.name).toBe('get_weather');
    expect(weatherTool.description).toBe('Get current weather for a location');
  });

  it('should convert to Vercel AI CoreTool correctly', async () => {
    const weatherTool = new MockWeatherTool();
    const mockContext: ToolContext = { sessionId: 's1', vaultName: 'main-vault' };
    
    const vercelTool = weatherTool.toVercelTool(mockContext) as any;
    
    expect(vercelTool.description).toBe('Get current weather for a location');
    
    // Test the bound execution function generated by toVercelTool
    const result = await vercelTool.execute({ location: 'Tokyo' });
    expect(result).toBe('Weather in Tokyo (Vault: main-vault) is Sunny.');
  });
});
